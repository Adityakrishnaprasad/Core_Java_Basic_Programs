package Patterns;

public class RectanglePattern {

	public static void main(String[] args)
	{
	  int n = 5;
	  int m = 4;
	//System.out.println("the pattern is .......");
		for(int a=1; a<=n; a++) {
			for(int b=1;b<=m;b++) {
				System.out.print(" $ ");
			}
			System.out.println();
		}
		

	}

}
