package TypeCasting;

public class Widening {

	public static void main(String[] args) 
	    {
	        float f =7.8f;
	        int d =(int) f;// widening--- is converting larger data type into smaller data type(explicitly) data loss will be there
	        System.out.println(f+"   "+d);
	        
	        Long L =987654321L;
	        double D = (double)L;
	        System.out.println(L+"  "+D);
	    
	        
	        
	        
	        
	        
			
	    }
	}


