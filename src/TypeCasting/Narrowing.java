package TypeCasting;

public class Narrowing {
	  static int z;
	  
	  static byte be;
	  static short se;
	  static long le;
	  static float fe;
	  static double de;
	  
	

	public static void main(String[] args) {
		
		//primitive and non primitive data types
		
		//primitive data types = byte<short<int<long<float<double
		
		System.out.println(".....local varialbles data is......");
		
		
	
		byte b=2;  //
		System.out.println(b);
		
		short s =12;  //(local variables)
		System.out.println(s);
		
		int a = 20;
		System.out.println(a);
		
		long k=1234567890l;//0.00L //it is used to store big numbers
		System.out.println(k);
		
		float f =2.2f;  //we use it for decimal numbers to store
		System.out.println(f);
		
		double d = 1234567890;
		System.out.println(d);
		
		char c = '1';
		System.out.println(c);
		
		char c1 = 'A';
		System.out.println(c1);
		
		char c2 = '&';
		System.out.println(c2);
		
		String name ="vara_prasad";
		System.out.println(name);
		
		boolean bn = true;
		System.out.println(bn);
		
		System.out.println("..... global variables data is ......");
		
		System.out.println(be);
		System.out.println(se);
		System.out.println(z);
		System.out.println(le);
		System.out.println(fe);
		System.out.println(de);
		
		
		
		
		
		
		
	}
}
